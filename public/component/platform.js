class Solid {
    constructor(x, y,img) {
      this.x = x;
      this.y =y;
      this.img = img
      this.size = 35;
      this.sheetSize = 40;
    }
    draw(offset, yOffset) {
      image(tiles, this.x - offset, this.y - yOffset, this.size, this.size, this.img[0] * this.sheetSize, this.img[1] * this.sheetSize, this.sheetSize, this.sheetSize);
    }
  }

  class Map {
    constructor(arr, size) {
  
      this.map = arr;
      this.offset = 0;
      this.bsize = size
  
      for (var row = 0; row < this.map.length; row++) {
        for (var col = 0; col < this.map[row].length; col++) {
          if (this.map[row][col] == 1) {
            this.map[row][col] = new Solid(col * this.bsize, row * this.bsize, [0.22, 0.2]);
  
          }
          if (this.map[row][col] == 2) {
            this.map[row][col] = new Solid(col * this.bsize, row * this.bsize, [6.7, 0.2]);
          
        }
        if (this.map[row][col] == 3) {
            this.map[row][col] = new Solid(col * this.bsize, row * this.bsize, [5.66, 3.45]);
          
        }
        if (this.map[row][col] == 4) {
            this.map[row][col] = new Solid(col * this.bsize, row * this.bsize, [8.9, 2.35]);
        }
      
      }
    }
    }

    draw() {
      for (var row = 0; row < this.map.length; row++) {
        for (var col = 0; col < this.map[row].length; col++) {
          if (this.map[row][col] != 0) {
            this.map[row][col].draw(this.offset, 0);
  
          }
        }
  
      }
  
    }
  }